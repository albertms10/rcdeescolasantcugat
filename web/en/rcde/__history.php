        <h3 id="yellow-clubs-original-colour" class="mt-5">Yellow, club’s original colour</h3>
        <hr class="divider my-4 ml-0">
        <p>Despite RCD Espanyol of Barcelona nowadays dresses white and blue, yellow was the club original colour, for
           the simple reason that was the colour of the piece of material that one of the first season ticket holder
           gave to the club to make this first kits. Further on, they wore white t-shirt and blue trousers and, in
            <time datetime="1909">1909,</time>
           they changed to the current colours white and but, the ones Roger de Llúria showed in his shield.
        </p>

        <h3 id="great-players" class="mt-5">Great players</h3>
        <hr class="divider my-4 ml-0">
        <p>For well over 100 years, Espanyol has had players well-known around the world. Possibly the first and great
           blue and white idol was the goal-keeper Ricardo Zamora. After him, other players such as the international
           Alfredo di Stefano, Kubala, N’Kono… joined the club.</p>

        <h3 id="great-football-academy" class="mt-5">Great football academy</h3>
        <hr class="divider my-4 ml-0">
        <p>The club counts with a second team, Espanyol B, twelve Academy teams, ladies’ teams and a football school.
           All
           of them train and play their matches as home teams in Dani Jarque’s training facilities, inaugurated on
            <time datetime="2001-09">September 2001.</time>
        </p>

        <h3 id="from-sarria-to-the-olympic-stadium" class="mt-5">From Sarrià to the Olympic Stadium</h3>
        <hr class="divider my-4 ml-0">
        <p>The present RCD Espanyol started playing their matches in a big esplanade, near to the Sagrada Família in
           Barcelona. From
            <time datetime="1923">1923</time>
           and to the season
            <time datetime="1996">1996–97,</time>
           Sarrià Stadium, familiarly known as ‘Can Ràbia’ or ‘la Bombonera’, at Ricardos Zamora’s square was the club’s
           ground.
        </p>
        <p>In
            <time datetime="1997">1997</time>
           the club was forced to sell Sarria and moved to the Lluís Companys’ Olympic Stadium, where RCD
           Espanyol played their matches until
            <time datetime="2008">2008–09</time>
           season with a regular assistance of 22,500 people per match.
           During these twelve years, the team was able to achieve great success as the Spanish Cup
            <time datetime="2000">(2000</time>
           and
            <time datetime="2006">2006)</time>
           or when we qualified to the UEFA Final Cup in
            <time datetime="2007">2007,</time>
           tournament in which they didn’t register any defeat.
        </p>
        <p>In Montjuïc we also lived wonderful moments like the team's salvation in
            <time datetime="2003">2003–04</time>
           thanks to Tamudo's goal against Murcia, Coro's goal in injury time against Real Sociedad that allowed RCD
           Espanyol to continue in the First Division in the year
            <time datetime="2006">2006</time>
           and face the way to our new house in Cornellà&thinsp;–&thinsp;El Prat, where the club moved on
            <time datetime="2009-07-10">July 10th 2009.</time>
        </p>

